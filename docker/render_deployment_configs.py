class RenderError(RuntimeError):
    """Raised when rendering inputs are invalid."""


def _load_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _load_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def _normalize_description(description: Any) -> str:
    normalized = " ".join(str(description or "").split())
    return re.sub(r"([.!?;:])(?=[A-Za-z`])", r"\1 ", normalized)


def _validate_deployment_mode(variable_name: str, deployment: Any, scope_name: str) -> str:
    if not isinstance(deployment, dict):
        raise RenderError(
            f"{variable_name} is missing deployment metadata. Regenerate the env references before rendering."
        )
    mode = deployment.get(scope_name)
    if mode not in {"required", "omit"}:
        raise RenderError(f"{variable_name} has invalid deployment metadata: {scope_name}={mode!r}")
    return mode


def _load_reference_variables(path: Path) -> list[dict[str, Any]]:
    payload = _load_json(path)
    variables = payload.get("variables")
    if not isinstance(variables, list):
        raise RenderError(f"{path.name} does not contain a variables list")
    for variable in variables:
        if not isinstance(variable, dict) or "name" not in variable:
            raise RenderError(f"{path.name} contains an invalid variable entry")
        _validate_deployment_mode(variable["name"], variable.get("deployment"), "main_example")
        _validate_deployment_mode(variable["name"], variable.get("deployment"), "middleware_example")
    return variables


def _load_required_middleware_env_entries() -> list[dict[str, Any]]:
    payload = _load_json(MIDDLEWARE_ENV_CONFIG_PATH)
    variables = payload.get("variables", [])
    if not isinstance(variables, list):
        raise RenderError(f"{MIDDLEWARE_ENV_CONFIG_PATH.name} does not contain a variables list")

    required_entries: list[dict[str, Any]] = []
    for variable in variables:
        if not isinstance(variable, dict) or "name" not in variable:
            raise RenderError(f"{MIDDLEWARE_ENV_CONFIG_PATH.name} contains an invalid variable entry")
        mode = variable.get("middleware_example", "omit")
        if mode not in {"required", "omit"}:
            raise RenderError(
                f"{MIDDLEWARE_ENV_CONFIG_PATH.name} has invalid middleware_example for {variable['name']}: {mode!r}"
            )
        if mode == "required":
            required_entries.append(variable)
    return required_entries


def _required_reference_variables(variables: list[dict[str, Any]], scope_name: str) -> list[dict[str, Any]]:
    return [
        variable
        for variable in variables
        if _validate_deployment_mode(variable["name"], variable.get("deployment"), scope_name) == "required"
    ]


def _append_env_section(lines: list[str], title: str, variables: list[dict[str, Any]]) -> None:
    if not variables:
        return
    if len(lines) > 1:
        lines.append("")
    lines.extend([f"# {title}", ""])

    for index, variable in enumerate(variables):
        description = _normalize_description(variable.get("description"))
        if description:
            lines.append(f"# {description}")
        lines.append(f"{variable['name']}=")
        if index != len(variables) - 1:
            lines.append("")


def _append_middleware_env_section(lines: list[str], variables: list[dict[str, Any]]) -> None:
    if not variables:
        return
    if len(lines) > 1:
        lines.append("")
    lines.extend(["# Docker middleware environment", ""])

    for index, variable in enumerate(variables):
        description = _normalize_description(variable.get("description"))
        if description:
            lines.append(f"# {description}")
        render_value = variable.get("render_value", "")
        lines.append(f"{variable['name']}={render_value}")
        if index != len(variables) - 1:
            lines.append("")


def _render_default_env_file() -> str:
    backend_variables = _load_reference_variables(BACKEND_REFERENCE_PATH)
    frontend_variables = _load_reference_variables(FRONTEND_REFERENCE_PATH)

    lines: list[str] = [ENV_WARNING_HEADER.rstrip("\n")]
    _append_env_section(lines, "Backend application environment", _required_reference_variables(backend_variables, "main_example"))
    _append_env_section(lines, "Frontend application environment", _required_reference_variables(frontend_variables, "main_example"))
    return "\n".join(lines).rstrip() + "\n"


def _render_middleware_env_file() -> str:
    backend_variables = _load_reference_variables(BACKEND_REFERENCE_PATH)
    frontend_variables = _load_reference_variables(FRONTEND_REFERENCE_PATH)
    docker_variables = _load_required_middleware_env_entries()

    lines: list[str] = [ENV_WARNING_HEADER.rstrip("\n")]
    _append_env_section(
        lines,
        "Backend application environment for middleware local development",
        _required_reference_variables(backend_variables, "middleware_example"),
    )
    _append_env_section(
        lines,
        "Frontend application environment for middleware local development",
        _required_reference_variables(frontend_variables, "middleware_example"),
    )
    _append_middleware_env_section(lines, docker_variables)
    return "\n".join(lines).rstrip() + "\n"


def _render_template(template: str, context: dict[str, str]) -> str:
    rendered = template
    for key, value in context.items():
        rendered = rendered.replace(f"{{{{{key}}}}}", value)

    unresolved = sorted(set(PLACEHOLDER_PATTERN.findall(rendered)))
    if unresolved:
        unresolved_display = ", ".join(unresolved)
        raise RenderError(f"Unresolved compose placeholders: {unresolved_display}")
    return rendered


def _cleanup_rendered_compose(text: str) -> str:
    text = re.sub(r"(\n[ ]+[A-Za-z0-9_\"'-]+:\n)\n([ ]+\S)", r"\1\2", text)
    return re.sub(r"\n{3,}", "\n\n", text)


