def _render_compose(target: str, template_path: Path) -> str:
    shared_services = _render_template(_load_text(SHARED_SERVICES_TEMPLATE_PATH), _shared_services_context(target))
    rendered = _render_template(_load_text(template_path), {"SHARED_DOCKER_SERVICES": shared_services})
    cleaned = _cleanup_rendered_compose(rendered)
    return f"{COMPOSE_WARNING_HEADER}\n{cleaned.lstrip()}"


def _write_if_changed(path: Path, content: str) -> bool:
    previous = path.read_text(encoding="utf-8") if path.exists() else None
    if previous == content:
        return False
    with path.open("w", encoding="utf-8", newline="\n") as file:
        file.write(content)
    return True


def _check_up_to_date(path: Path, expected: str) -> bool:
    return path.exists() and path.read_text(encoding="utf-8") == expected


def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Render docker deployment config artifacts.")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Verify generated artifacts are up to date instead of rewriting them.",
    )
    return parser


def main() -> int:
    args = build_arg_parser().parse_args()

    rendered_default_env = _render_default_env_file()
    rendered_middleware_env = _render_middleware_env_file()
    rendered_main_compose = _render_compose("main", MAIN_TEMPLATE_PATH)
    rendered_middleware_compose = _render_compose("middleware", MIDDLEWARE_TEMPLATE_PATH)

    outputs = (
        (DEFAULT_ENV_OUTPUT_PATH, rendered_default_env),
        (MIDDLEWARE_ENV_OUTPUT_PATH, rendered_middleware_env),
        (MAIN_COMPOSE_OUTPUT_PATH, rendered_main_compose),
        (MIDDLEWARE_COMPOSE_OUTPUT_PATH, rendered_middleware_compose),
    )

    if args.check:
        stale_paths = [str(path) for path, content in outputs if not _check_up_to_date(path, content)]
        if stale_paths:
            for stale_path in stale_paths:
                print(f"Out of date: {stale_path}", file=sys.stderr)
            return 1
        return 0

    for path, content in outputs:
        _write_if_changed(path, content)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
